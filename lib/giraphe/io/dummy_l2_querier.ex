  def query_fdb(target) do
    fdb =
      target
        |> NetAddr.address
        |> _query_fdb
        |> Enum.map(fn {port, mac} -> {port, NetAddr.mac_48(mac)} end)

    case fdb do
      [] ->
        {:error, target, :fdb, :etimedout}

      _ ->
        {:ok, target, :fdb, fdb}
    end
  end

